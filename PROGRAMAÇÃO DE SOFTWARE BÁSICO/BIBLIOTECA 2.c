/* Funcao soma
	- Input:
		- a: inteiro
		- b: inteiro
	- Output:
		- a+b: inteiro*/
int soma(int a, int b)
{
	return a+b;
}

/* Funcao sub
	- Input:
		- a: inteiro
		- b: inteiro
	- Output:
		- a-b: inteiro*/
int sub(int a, int b)
{
	return a-b;
}